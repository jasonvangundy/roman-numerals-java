package katas;

public class Mapping {

    private int num;
    private String numeral;

    public Mapping(int num, String numeral) {
        this.num = num;
        this.numeral = numeral;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getNumeral() {
        return numeral;
    }

    public void setNumeral(String numeral) {
        this.numeral = numeral;
    }
}
